package uk.co.mruoc.race.gui;

public class ScaleParams {

    private double x;
    private double y;

    public ScaleParams(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

}
